package fr.formation.exo11.utensil;

/**
 * Interface définissant une assiette comme un objet ayant une méthode qui
 * permet de calculer sa surface.
 *
 * @author hb-asus
 *
 */
public interface Plate {

	/**
	 * Méthode permettant de calculer la surface de l'assiette.
	 *
	 * @return double la valeur de la surface calculée.
	 */
	public double calculateArea();
}
